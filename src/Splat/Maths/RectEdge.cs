namespace Splat;

/// <summary>
/// Specifies which edge of a rectangle to use.
/// </summary>
public enum RectEdge
{
    /// <summary>
    /// The left of the rectangle.
    /// </summary>
    Left,

    /// <summary>
    /// The top of the rectangle.
    /// </summary>
    Top,

    /// <summary>
    /// The right of the rectangle.
    /// </summary>
    Right,

    /// <summary>
    /// The bottom of the rectangle.
    /// </summary>
    Bottom,
}
